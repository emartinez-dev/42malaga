int	ft_str_is_alpha(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c < 65 || c > 122)
			return (0);
		if (c > 90 && c < 97)
			return (0);
		i++;
	}
	return (1);
}
